// показываем только те разделы, чьи страницы доступны пользователю
// (Settings и разделы без page видны всегда)
const sections = computed(() => allSections.filter((s) => !s.page || pageAllowed(s.page)))

const openSet = ref(new Set<number>())

function toggle(i: number) {
  if (openSet.value.has(i)) openSet.value.delete(i)
  else openSet.value.add(i)
}

// --- написать админу ---
const message = ref('')
const sending = ref(false)

async function send() {
  const text = message.value.trim()
  if (text.length < 3) {
    showToast('Напишите хотя бы пару слов')
    return
  }
  sending.value = true
  try {
    await api.post('/help/contact', { text })
    message.value = ''
    showToast('Отправлено! Админ получит уведомление 📨')
  } catch {
    showToast('Не удалось отправить, попробуйте позже')
  } finally {
    sending.value = false
  }
}
